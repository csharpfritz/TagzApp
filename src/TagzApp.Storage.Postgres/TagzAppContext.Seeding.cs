	/// <summary>
	/// Seeds the Locations table with common worldwide locations
	/// </summary>
	/// <returns>True if seeding was performed, false if data already exists</returns>
	public async Task<bool> SeedLocationsAsync()
	{
		// Check if data already exists
		if (await Locations.AnyAsync())
		{
			return false;
		}

		var locations = GetCommonLocations();

		await Locations.AddRangeAsync(locations);
		await SaveChangesAsync();

		return true;
	}

	/// <summary>
